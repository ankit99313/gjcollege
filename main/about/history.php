          Keeping this fact in mind, after the attainment of independence. It
          was felt that the education that helped to create to awareness during
          national movement for liberty could also help to achieve freedom from
          fear, want, diseases, ignorance, squalor and elimination of poverty by
          the application and development of knowledge. With the passage of time
          and all that came and went with it, the achievement of knowledge
          through meditation or penances lost their relevance. During nearly two
          centuries of British dominion and introduction of Macaley system, the
          significance of knowledge had changed from value to price. To acquire
          knowledge meant achieve it through academic institutions like schools,
          colleges and universities. Under such circumstances, an institution
          for higher education in Bihta a rural area of Patna district was
          assumed to be the need of the hour that may provide an opportunity to
          reflect on the critical , social, economical, cultural, moral and
          spiritual issues facing the rural masses of all categories. It was
          further felt, a college, if established will stand for humanism,
          increase in tolerance with reasoning, adventure of idea, search of
          truth thereby providing quality of leadership in politics,
          administration, profession, trade, commerce and industry.
