          With this mission in mind, Pram Pujya Sri GADADHARACHARYA alias MACHA
          BABA conceived the idea to start a college to impart education to the
          children of the poor people of this rural area. His brain child was
          translated on the ground with the help of local people in the form of
          Sri Gadadharachaya Janta College, Rambagh, Bihta (Patna) now popularly
          know as G. J. College, Rambagh, Bihta (Patna) as a proposed college in
          1958. It got affiliation on 21st June 1959 up to B.A. by then Bihar
          University. It got permanent affiliation upto B.A . in 1965 by Magadh
          University, Gaya under the stewardship of Late Awadhesh Kumar Sinha as
          the Principal of the college. This college got permission to start
          Hons. in Economics, Pol. Sc. and then in Hindi.
          <br />
          <br />
          Science education was started from 1969 in Phy, Chem, and Maths and
          from 1970 in Biology (Zoo. &amp; Bot.). This college got permanent
          affiliation in Science from 1974-75. On 1st April 1977 this college
          became a constituent unit of Magadh University, Bodh Gaya.
          <br />
          <br />
          The dream of its founder MACHA BABA has became a reality as this
          college is imparting degree level education in Arts and Science up to
          Hons. to the rural population of this area. This college has completed
          50 years and in this Golden Jubilee year it is going to add one more
          feather to its cap by starting B. Com. Hons., teaching from the
          academic session 2009-10 which is the need of changing world.
