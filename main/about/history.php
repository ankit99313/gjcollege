          It is not easier to speak literal explanation of this wise saying of
          the aforesaid mystical saint. But in the light of his perceptive
          intellect it may be construed that nothing is possible without
          acquiring sufficiency and efficiency in the field of knowledge. It has
          been observed particularly since the remote antiquity that the name
          and fame earned by accumulating huge wealth or the post and position
          however high held by accumulating huge wealth or the post and position
          however high held by applying all sorts of tactics, trickery,
          maneuvering and manipulations do not provide such brightness, glory
          and gloss to any individual family or society as only education does.
          <br />
